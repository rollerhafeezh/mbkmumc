                <h3 class="">Apa itu Kampus Merdeka ?</h3>
                <article class="lh-lg">
                    <p>Program persiapan karier yang komprehensif guna mempersiapkan generasi terbaik Indonesia.</p>
                    <p>Kampus Merdeka merupakan bagian dari kebijakan Merdeka Belajar oleh Kementerian Pendidikan, Kebudayaan, Riset, dan Teknologi Republik Indonesia yang memberikan kesempaatan bagi mahasiswa/i untuk mengasah kemampuan sesuai bakat dan minat dengan terjun langsung ke dunia kerja sebagai persiapan karier masa depan.</p>
                    <p>Kenapa kamu harus mengikuti program-program Kampus Merdeka?</p>

                    <ol>
                        <li>Kegiatan praktik di lapangan akan dikonversi menjadi SKS</li>
                        <li>Eksplorasi pengetahuan dan kemampuan di lapangan selama lebih dari satu semester</li>
                        <li>Belajar dan memperluas jaringan di luar program studi atau kampus asal</li>
                        <li>Menimba ilmu secara langsung dari mitra berkualitas dan terkemuka</li>
                    </ol>
                </article>

                <h3 class="mt-4">Latar Belakang</h3>
                <article class="lh-lg">
                    <p>Dalam rangka menyiapkan mahasiswa menghadapi perubahan sosial, budaya, dunia kerja dan kemajuan teknologi yang pesat, kompetensi mahasiswa harus disiapkan untuk lebih gayut dengan kebutuhan zaman. Link and match tidak saja dengan dunia industri dan dunia kerja tetapi juga dengan masa depan yang berubah dengan cepat. Perguruan Tinggi dituntut untuk dapat merancang dan melaksanakan proses pembelajaran yang inovatif agar mahasiswa dapat meraih capaian pembelajaran mencakup aspek sikap, pengetahuan, dan keterampilan secara optimal dan selalu relevan.</p>

                    <p>Kebijakan Merdeka Belajar - Kampus Merdeka diharapkan dapat menjadi jawaban atas tuntutan tersebut. Kampus Merdeka merupakan wujud pembelajaran di perguruan tinggi yang otonom dan fleksibel sehingga tercipta kultur belajar yang inovatif, tidak mengekang, dan sesuai dengan kebutuhan mahasiswa.</p>

                    <p>Program utama yaitu: kemudahan pembukaan program studi baru, perubahan sistem akreditasi perguruan tinggi, kemudahan perguruan tinggi negeri menjadi PTN berbadan hukum, dan hak belajar tiga semester di luar program studi. Mahasiswa diberikan kebebasan mengambil SKS di luar program studi, tiga semester yang di maksud berupa 1 semester kesempatan mengambil mata kuliah di luar program studi dan 2 semester melaksanakan aktivitas pembelajaran di luar perguruan tinggi.</p>

                    <p>Berbagai bentuk kegiatan belajar di luar perguruan tinggi, di antaranya melakukan magang/praktik kerja di Industri atau tempat kerja lainnya, melaksanakan proyek pengabdian kepada masyarakat di desa, mengajar di satuan pendidikan, mengikuti pertukaran mahasiswa, melakukan penelitian, melakukan kegiatan kewirausahaan, membuat studi/ proyek independen, dan mengikuti program kemanusisaan. Semua kegiatan tersebut harus dilaksanakan dengan bimbingan dari dosen. Kampus merdeka diharapkan dapat memberikan pengalaman kontekstual lapangan yang akan meningkatkan kompetensi mahasiswa secara utuh, siap kerja, atau menciptakan lapangan kerja baru.</p>

                    <p>Proses pembelajaran dalam Kampus Merdeka merupakan salah satu perwujudan pembelajaran yang berpusat pada mahasiswa (student centered learning) yang sangat esensial. Pembelajaran dalam Kampus Merdeka memberikan tantangan dan kesempatan untuk pengembangan inovasi, kreativitas, kapasitas, kepribadian, dan kebutuhan mahasiswa, serta mengembangkan kemandirian dalam mencari dan menemukan pengetahuan melalui kenyataan dan dinamika lapangan seperti persyaratan kemampuan, permasalahan riil, interaksi sosial, kolaborasi, manajemen diri, tuntutan kinerja, target dan pencapaiannya. Melalui program merdeka belajar yang dirancang dan diimplementasikan dengan baik, maka hard dan soft skills mahasiswa akan terbentuk dengan kuat.</p>

                    <p>Program Merdeka Belajar - Kampus Merdeka diharapkan dapat menjawab tantangan Perguruan Tinggi untuk menghasilkan lulusan yang sesuai perkembangan zaman, kemajuan IPTEK, tuntutan dunia usaha dan dunia industri, maupun dinamika masyarakat.</p>
                </article>

                <h3 class="mt-4">Tujuan</h3>
                <article class="lh-lg">
                    <p>Tujuan kebijakan Merdeka Belajar - Kampus Merdeka, program “hak belajar tiga semester di luar program studi” adalah untuk meningkatkan kompetensi lulusan, baik soft skills maupun hard skills, agar lebih siap dan relevan dengan kebutuhan zaman, menyiapkan lulusan sebagai pemimpin masa depan bangsa yang unggul dan berkepribadian. Program-program experiential learning dengan jalur yang fleksibel diharapkan akan dapat memfasilitasi mahasiswa mengembangkan potensinya sesuai dengan passion dan bakatnya.</p>
